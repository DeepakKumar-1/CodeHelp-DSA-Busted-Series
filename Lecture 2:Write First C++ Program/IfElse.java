package com.company;

public class IfElse {
    public static void main(String[] args) {
        if(true){
            System.out.println("Hello World!");
        }

        // Print First Five Numbers
        int count = 0;
        while(count<=10){
            System.out.print(count + " ");
            count++;
        }

        System.out.print("\n");
        // For Loop
        for(int counter = 0;counter<=10;counter++){
            System.out.print(counter + " ");
        }
    }
}
