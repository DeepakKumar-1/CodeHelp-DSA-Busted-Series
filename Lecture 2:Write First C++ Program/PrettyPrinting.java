package com.company;

public class PrettyPrinting {
    // Pretty Printing
    public static void main(String [] args){
        float a = 453.1234f;
        System.out.printf(" Formatted Number is %.2f \n",a);  // it Also Round it as Well

        System.out.println(Math.PI);
        System.out.printf(" PI : %.3f", Math.PI);

        System.out.printf("\n Hello I Am %s and I am %s ", "Deepak", "Cool");

        // For More Format Specifiers go to Placeholders.txt
    }
}
